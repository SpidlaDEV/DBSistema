            <li><p><b>1827:</b> - Se casa con Demarius Lamb y se muda a la cercana Vergennes, donde Deere trabaja con el herrero John McVene.</p></li>
            <li><p><b>1837:</b> - Desarrolla el primer arado de acero autolimpiante comercialmente exitoso</p></li>
            <li><p><b>1838:</b> - En Grand Detour, Deere se une a su esposa y cinco hijos, incluido el nuevo bebé Charles. Continúa en su negocio general de herrería, produciendo solo unos pocos arados antes de pasar al negocio de arados a tiempo completo.</p></li>
            <li><p><b>1841:</b> - Deere fabrica 75 arados</p></li>
            <li><p><b>1848:</b> - Traslada la empresa a Moline, Illinois, y se convierte en socio de Robert Tate y John Gould. La familia Deere llega de Grand Detour y se muda a una pequeña casa con Robert Tate, su esposa y su hija. Charles Deere, de once años, recordó más tarde haber dormido en un baúl en el pasillo.</p></li>
            <li><p><b>1849:</b> - La fuerza laboral de Deere de alrededor de 16 construye 2136 arados. La familia Deere se mudó a una casa grande a varias cuadras de Plough Works en Main Street en Moline.</p></li>
            <li><p><b>1852:</b> - Se disuelve Deere, Tate & Gould</p></li>
            <li><p><b>1854:</b> - Ayuda a organizar la Moline Property Protection Society para "protegernos de la depredación de los ladrones"</p></li>
            <li><p><b>1855:</b> - Apaga un incendio en el puente de Rock Island con un tal Sr. Thompson de Quincy, Illinois. Más tarde ese año dirige los esfuerzos para llevar un camión de bomberos a Moline. </p></li>
            <li><p><b>1858:</b> - Junto con otros abolicionistas, disuelve una reunión del Partido Demócrata en Rock Island</p></li>
            <li><p><b>1861:</b> - Promete $10 para sufragar los costos de formar una compañía de 100 días de voluntarios de Moline para el servicio durante la Guerra Civil. Su hijo, Charles, también se compromete. </p></li>
            <li><p><b>1863:</b> - Ayuda a organizar el First National Bank of Moline con otros empresarios locales, incluidos los ex socios Charles Atkinson y John Gould. Se convierte en presidente en 1866. </p></li>
            <li><p><b>1864:</b> - En enero, John Deere obtiene su primera patente (y la primera de la empresa), una "mejora nueva y útil en moldes para fundición de arados de acero y otros artículos". Modifica la patenteen abril y recibe dos patentes más en los próximos tres años. </p></li>
            <li><p><b>1864:</b> - Compra una granja en un acantilado en el este de Moline, y la llama Alderney Hill Farm, en honor a una popular raza de ganado. Es una de las muchas granjas que posee en Moline, junto con varias otras en todo el país. </p></li>
            <li><p><b>1865:</b> - Muere Demarius Deere, la esposa de John durante 38 años</p></li>
            <li><p><b>1866:</b> - Se casa con Lucenia Lamb, hermana menor de su ex esposa Demarius</p></li>
            <li><p><b>1868:</b> - Compra una casa en Santa Bárbara, California, donde él y Lucenia pasan la mayor parte de sus inviernos</p></li>
            <li><p><b>1868:</b> - Se incorpora Deere & Company. John es nombrado presidente, con su hijo Charles, su sobrino George Vinton y su yerno Stephen Velie como accionistas restantes. </p></li>
            <li><p><b>1873:</b> - Elegido segundo alcalde de Moline, Illinois, ganando con 601 de los 626 votos totales emitidos. Sirve un término de 2 años. Ese año también es nombrado Director Vitalicio de la American Home Missionary Society, con sede en Chicago. </p></li>
            <li><p><b>1874:</b> - Bajo la dirección de Deere, Melvin A. Gould diseña la primera marca comercial de ciervos saltadores</p></li>
            <li><p><b>1879:</b> - Nombrado fideicomisario en el alma mater de su hijo, Knox College en Galesburg, Illinois</p></li>
            <li><p><b>1882:</b> - Sorprendido por toda la oficina de Deere & Company, que caminó desde su sede en el centro de la ciudad hasta su casa para sorprenderlo el 7 de febrero, su 78 cumpleaños</p></li>
            <li><p><b>1885:</b> - A los 81 años, asiste a la Feria Estatal de Minnesota con su nieto, C.C. Webber</p></li>
            <li><p><b>1886:</b> - Se escritura terreno en el centro de Moline para una plaza de mercado</p></li>
            <li><p><b>1886:</b> - Muere el 17 de mayo, a la edad de 82 años, en su casa de Moline, Red Cliff</p></li>
